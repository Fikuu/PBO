package lat3;

/**
 *
 * @author Fikri
 */
public class BangunDatar {
    
    protected int a;
    protected int t;
    
    public BangunDatar(){
        this.a=0;
        this.t=0;
    }

    public BangunDatar(int a, int t) {
        this.a = a;
        this.t = t;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }
    
    
}
